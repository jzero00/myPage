package com.mypage.vo;

public class MenuVO {
	
	private String mcode;
	private String code_name;
	private String url;
	private String icon_file;
	private String upcode;
	private int enable;
	private int iscategory;
	private String icon_color;
	private int code_level;
	private String js;
	
	public String getMcode() {
		return mcode;
	}
	public void setMcode(String mcode) {
		this.mcode = mcode;
	}
	public String getCode_name() {
		return code_name;
	}
	public void setCode_name(String code_name) {
		this.code_name = code_name;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public String getIcon_file() {
		return icon_file;
	}
	public void setIcon_file(String icon_file) {
		this.icon_file = icon_file;
	}
	public String getUpcode() {
		return upcode;
	}
	public void setUpcode(String upcode) {
		this.upcode = upcode;
	}
	public int getEnable() {
		return enable;
	}
	public void setEnable(int enable) {
		this.enable = enable;
	}
	public int getIscategory() {
		return iscategory;
	}
	public void setIscategory(int iscategory) {
		this.iscategory = iscategory;
	}
	public String getIcon_color() {
		return icon_color;
	}
	public void setIcon_color(String icon_color) {
		this.icon_color = icon_color;
	}
	public int getCode_level() {
		return code_level;
	}
	public void setCode_level(int code_level) {
		this.code_level = code_level;
	}
	public String getJs() {
		return js;
	}
	public void setJs(String js) {
		this.js = js;
	}
	
	
}
